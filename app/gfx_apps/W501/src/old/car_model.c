#define NEEDLE_ACCELERATION 1.0

void CarModelInit(void)
{
    cmTickStartTime = R_TICK_GetTimeMS(0);
    cmCurrentTime   = 0;
    cmCurrentField  = 0;
    currentKPH      = 0;               
    currentRPM      = 0;    
    //carModelData    = car_model_data;
}

void CarModelGetData(float *kph, float *rpm)
{
    uint32_t tDiff;
    float64_t kphDiff;
    float64_t rpmDiff;
    
    float tFact;
     
    cmCurrentTime = (R_TICK_GetTimeMS(0) - cmTickStartTime) * NEEDLE_ACCELERATION;
    while(cmCurrentTime > carModelData[cmCurrentField+1].cmTime)
    {
        cmCurrentField++;
        if((carModelData[cmCurrentField].cmTime == 0) &&
           (carModelData[cmCurrentField].cmRPM == 0) &&
           (carModelData[cmCurrentField].cmKPH == 0) &&
           (cmCurrentField != 0))
        {
            /* All Values zero -> end reached */
            CarModelInit();
        }
    }
    

    
    tDiff = carModelData[cmCurrentField+1].cmTime - carModelData[cmCurrentField].cmTime;
    tFact = (float)(((float)cmCurrentTime - (float)carModelData[cmCurrentField].cmTime) / (float)tDiff);
    
    kphDiff = (carModelData[cmCurrentField+1].cmKPH - carModelData[cmCurrentField].cmKPH) * tFact;
    rpmDiff = (carModelData[cmCurrentField+1].cmRPM - carModelData[cmCurrentField].cmRPM) * tFact;   
    
    currentKPH = carModelData[cmCurrentField].cmKPH + kphDiff;
    currentRPM = carModelData[cmCurrentField].cmRPM + rpmDiff;
    
    *kph = (float)currentKPH / 10.0f;
    *rpm = (float)currentRPM;
}
